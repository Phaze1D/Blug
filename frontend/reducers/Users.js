import * as types from '../actions/ActionTypes'

export const userNewReducer = (state, action) => {
  switch (action.type) {
    case `${types.USER_NEW}_LOADING`:

      break;

    case `${types.USER_NEW}_SUCCESS`:

      break;

    case `${types.USER_NEW}_ERROR`:

      break;


  }
}


